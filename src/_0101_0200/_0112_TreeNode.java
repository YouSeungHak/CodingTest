package _0101_0200;

// Definition for a binary tree node.
public class _0112_TreeNode {
    int val;
    _0112_TreeNode left;
    _0112_TreeNode right;
    _0112_TreeNode() {
    }
    _0112_TreeNode(int val) {
        this.val = val;
    }
    _0112_TreeNode(int val, _0112_TreeNode left, _0112_TreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
}
